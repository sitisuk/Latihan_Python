#fungsi di bawah ini fungsi untuk menambahkan dua bilangan dan mengembalikan hasilnya

def penjumlahan(a,b) :
    hasil = a + b
    return hasil
def pengurangan (a,b) :
    hasil = a - b
    return hasil
def perkalian (a,b) :
    hasil = a * b
    return hasil
def pembagian (a,b) :
    hasil = a / b
    return hasil 